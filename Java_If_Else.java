package c251117.c251117_java_basic;

public class Java_If_Else {

    public static void main(String[] args) {

        double cg = 3.5;
        if (cg >= 3.5) {
            System.out.println("Your are Good at CGPA: " + cg);
        } else {
            System.out.println("Your are Average at CGPA: " + cg);
        }
    }
}
