package c251117.c251117_java_basic;

public class Java_Switch {

    public static void main(String[] args) {

        int day = 3;
        switch (day) {
            case 1:
                System.out.println("This is Monday");
                break;
            case 2:
                System.out.println("This is Tuesday");
                break;
            default:
                System.out.println("This is Other day");
        }
    }
}
